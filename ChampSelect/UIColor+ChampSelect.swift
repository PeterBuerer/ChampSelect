import UIKit

extension UIColor {
    class func backgroundPrimary() -> UIColor {
        return UIColor(red:0.17, green:0.05, blue:0.23, alpha:1) //Violet
    }
    
    class func cellPrimary() -> UIColor {
        return UIColor.whiteColor()
    }
}